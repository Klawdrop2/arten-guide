class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('selectedLanguage') || 'en';
        this.init();
    }
    
    init() {
        document.addEventListener('DOMContentLoaded', () => {
            this.createLanguageSelector();
            this.translatePage();
        });
    }
    
    createLanguageSelector() {
        const headerActions = document.querySelector('.header-actions');
        if (!headerActions) return;
        
        const languageSelector = document.createElement('div');
        languageSelector.className = 'language-selector';
        languageSelector.innerHTML = `
            <button class="btn secondary language-btn" id="language-btn">
                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
                    <circle cx="12" cy="12" r="10"/>
                    <path d="M2 12h20M12 2a15.3 15.3 0 0 1 4 10 15.3 15.3 0 0 1-4 10 15.3 15.3 0 0 1-4-10 15.3 15.3 0 0 1 4-10z"/>
                </svg>
                <span data-translate="language">Language</span>
                <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" class="dropdown-arrow">
                    <polyline points="6,9 12,15 18,9"/>
                </svg>
            </button>
            <div class="language-dropdown" id="language-dropdown">
                <div class="language-option" data-lang="en">
                    <span class="flag">🇺🇸</span>
                    English
                </div>
                <div class="language-option" data-lang="fr">
                    <span class="flag">🇫🇷</span>
                    Français
                </div>
                <div class="language-option" data-lang="de">
                    <span class="flag">🇩🇪</span>
                    Deutsch
                </div>
            </div>
        `;
        
        headerActions.insertBefore(languageSelector, headerActions.firstChild);
        
        const languageBtn = document.getElementById('language-btn');
        const languageDropdown = document.getElementById('language-dropdown');
        const languageOptions = document.querySelectorAll('.language-option');
        
        languageBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            languageDropdown.classList.toggle('active');
        });
        
        document.addEventListener('click', () => {
            languageDropdown.classList.remove('active');
        });
        
        languageOptions.forEach(option => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const selectedLang = option.dataset.lang;
                this.setLanguage(selectedLang);
                languageDropdown.classList.remove('active');
            });
        });
        
        const activeOption = document.querySelector(`[data-lang="${this.currentLanguage}"]`);
        if (activeOption) {
            activeOption.classList.add('active');
        }
    }
    
    setLanguage(language) {
        this.currentLanguage = language;
        localStorage.setItem('selectedLanguage', language);
        
        document.querySelectorAll('.language-option').forEach(option => {
            option.classList.remove('active');
        });
        const activeOption = document.querySelector(`[data-lang="${language}"]`);
        if (activeOption) {
            activeOption.classList.add('active');
        }
        
        this.translatePage();
        
        document.documentElement.lang = language;
        
        if (typeof refreshGuideLanguage === 'function') {
            refreshGuideLanguage();
        }
    }
    
    translatePage() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.getTranslation(key);
            if (translation) {
                element.textContent = translation;
            }
        });
        
        const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            const translation = this.getTranslation(key);
            if (translation) {
                element.placeholder = translation;
            }
        });
        
        const titleElements = document.querySelectorAll('[data-translate-title]');
        titleElements.forEach(element => {
            const key = element.getAttribute('data-translate-title');
            const translation = this.getTranslation(key);
            if (translation) {
                element.title = translation;
            }
        });
    }
    
    getTranslation(key, replacements = {}) {
        const translation = translations[this.currentLanguage]?.[key] || translations['en']?.[key] || key;
        
        let result = translation;
        Object.keys(replacements).forEach(placeholder => {
            result = result.replace(new RegExp(`\\{${placeholder}\\}`, 'g'), replacements[placeholder]);
        });
        
        return result;
    }
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
}

const languageManager = new LanguageManager();
